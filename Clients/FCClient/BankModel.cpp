#include "BankModel.h"

BankModel* BankModel::m_pThis = NULL;

///////////////////////////////////////////////////////////////////////

BankModel::BankModel(void)
: m_bLoggedIn(false)
{
}

///////////////////////////////////////////////////////////////////////

BankModel::~BankModel(void)
{
}

///////////////////////////////////////////////////////////////////////

BankModel* BankModel::instance()
{
  if ( !m_pThis )
  {
    m_pThis = new BankModel;
  }

  return m_pThis;
}

///////////////////////////////////////////////////////////////////////

void BankModel::destroy()
{
  if ( m_pThis)
  {
    delete m_pThis;
    m_pThis = NULL;
  }
}

